CREATE TABLE Users (
    id INT AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(50) NOT NULL,
    passwrd VARCHAR(50) NOT NULL
);

CREATE TABLE Comments (
	id INT AUTO_INCREMENT PRIMARY KEY,
	comment VARCHAR(200) NOT NULL,
	category VARCHAR(50) NOT NULL,
	votes INT NOT NULL
);

CREATE TABLE Votes (
    id INT AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(50) NOT NULL,
    commentID INT NOT NULL
);